function thread(partial: Omit<CommunicationThread, 'tenantId' | 'createdAt' | 'updatedAt'>): CommunicationThread {
  return {
    tenantId: TENANT,
    createdAt: daysAgo(30),
    updatedAt: partial.lastMessageAt ?? NOW,
    ...partial,
  };
}

