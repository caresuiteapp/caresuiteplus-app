    isInternalNote: false,
    isVisibleToBusiness: true,
    isVisibleToEmployee: false,
    isVisibleToClient: false,
    isVisibleToRelative: false,
    sentAt: null,
    deliveredAt: null,
    readAt: null,
    editedAt: null,
    editedBy: null,
    deletedAt: null,
    deletedBy: null,
    deleteReason: null,
    replyToMessageId: null,
