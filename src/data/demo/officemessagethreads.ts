let mutableDemoThreads = [...demoOfficeMessageThreads];
let mutableDemoMessages = [...demoOfficeMessages];

export function getDemoOfficeMessageThreads(): OfficeMessageThread[] {
  return mutableDemoThreads.map((thread) => ({ ...thread }));
}

export function getDemoOfficeMessages(): OfficeMessage[] {
  return mutableDemoMessages.map((message) => ({ ...message }));
}

export function resetDemoOfficeMessaging(): void {
  mutableDemoThreads = [...demoOfficeMessageThreads];
  mutableDemoMessages = [...demoOfficeMessages];
}

export function appendDemoOfficeMessage(message: OfficeMessage): void {
  mutableDemoMessages.push(message);
  const threadIndex = mutableDemoThreads.findIndex((thread) => thread.id === message.threadId);
  if (threadIndex >= 0) {
    const thread = mutableDemoThreads[threadIndex];
    mutableDemoThreads[threadIndex] = {
      ...thread,
      lastMessageAt: message.sentAt,
      lastMessagePreview: message.body.slice(0, 120),
      updatedAt: message.sentAt,
      unreadCount: message.senderType !== 'office_profile' ? thread.unreadCount + 1 : thread.unreadCount,
    };
  }
}

export function appendDemoOfficeThread(thread: OfficeMessageThread): void {
  mutableDemoThreads.unshift(thread);
}
